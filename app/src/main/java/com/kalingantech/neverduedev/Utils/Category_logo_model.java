package com.kalingantech.neverduedev.Utils;

public class Category_logo_model {
    int cat_icon;
    String cat_name;

    public Category_logo_model(int cat_icon, String cat_name) {
        this.cat_icon = cat_icon;
        this.cat_name = cat_name;
    }

    public int getCat_icon() {
        return cat_icon;
    }

    public void setCat_icon(int cat_icon) {
        this.cat_icon = cat_icon;
    }

    public String getCat_name() {
        return cat_name;
    }

    public void setCat_name(String cat_name) {
        this.cat_name = cat_name;
    }
}






